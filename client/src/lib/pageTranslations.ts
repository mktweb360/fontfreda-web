export type PageTranslationKey = keyof typeof pageTranslations;

export function getPageTranslation(page: PageTranslationKey, key: string, language: "es" | "en"): string {
  const pageData = pageTranslations[page];
  if (!pageData) return key;
  const langData = pageData[language];
  if (!langData) return key;
  return (langData as Record<string, string>)[key] || key;
}
